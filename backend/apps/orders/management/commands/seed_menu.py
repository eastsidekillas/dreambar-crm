class Command(BaseCommand):
    help = 'Seed full BAR DREAM menu and create users'

    def handle(self, *args, **options):
        from apps.orders.models import UserProfile

        # Employees: (username, password, display_name, role)
        staff = [
            ('admin',     'dreambar2026', 'Администратор',   'admin'),
            ('waiter1',   'waiter2026',   'Официант Анна',   'waiter'),
            ('waiter2',   'waiter2026',   'Официант Иван',   'waiter'),
            ('bartender', 'bar2026',      'Бармен Сергей',   'bartender'),
            ('kitchen',   'kuhnya2026',   'Повар Дмитрий',   'kitchen'),
            ('wardrobe',  'gard2026',     'Гардероб Ольга',  'wardrobe'),
        ]
        for username, password, display, role in staff:
            user, created = User.objects.get_or_create(username=username, defaults={
                'first_name': display,
                'is_staff': role == 'admin',
                'is_superuser': username == 'admin',
            })
            if created:
                user.set_password(password)
                user.save()
                self.stdout.write(self.style.SUCCESS(f'{username} / {password} ({role})'))
            profile, _ = UserProfile.objects.get_or_create(user=user)
            profile.role = role
            profile.display_name = display
            profile.save()

        total_items = 0
        sort_cat = 0
        for cat_type, categories in MENU_DATA.items():
            for cat_data in categories:
                cat, _ = MenuCategory.objects.get_or_create(
                    name=cat_data['name'],
                    type=cat_type,
                    defaults={'sort_order': sort_cat}
                )
                sort_cat += 1
                for i, item_tuple in enumerate(cat_data['items']):
                    name, volume, price, description = item_tuple
                    MenuItem.objects.get_or_create(
                        name=name,
                        category=cat,
                        volume=volume,
                        defaults={
                            'price': price,
                            'description': description,
                            'sort_order': i,
                        }
                    )
                    total_items += 1

        self.stdout.write(self.style.SUCCESS(
            f'Меню загружено: {MenuCategory.objects.count()} категорий, {MenuItem.objects.count()} позиций'
        ))
